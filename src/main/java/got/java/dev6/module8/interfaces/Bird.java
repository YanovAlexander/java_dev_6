package got.java.dev6.module8.interfaces;

public abstract class Bird {
    private String color;
    private int paw;

    public void setColor(String color) {
        this.color = color;
    }

    public void setPaw(int paw) {
        this.paw = paw;
    }

    public String getColor() {
        return color;
    }

    public int getPaw() {
        return paw;
    }

}
